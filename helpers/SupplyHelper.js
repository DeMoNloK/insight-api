var BigNumber = require('bignumber.js');

module.exports = {
    /**
     *
     * @param {Number} height
     * @return {BigNumber}
     */
    getTotalSupplyByHeight: function (height) {

        var coins;
        if (height == 0) {
            coins = 0;
        } else if (height < 200001) {
            coins = height * 12500;
        } else if (height < 300001) {
            coins = height * 2500 + 200001 * 12500;
        } else if (height < 400001) {
            coins = height * 1250 + 200001 * 12500 + (300001 - 200001) * 2500;
        } else if (height < 500001) {
            coins = height * 625 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250;
        } else if (height < 600001) {
            coins = height * 312.5 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625;
        } else if (height < 700001) {
            coins = height * 156.25 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5;
        } else if (height < 800001) {
            coins = height * 78 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5 + (700001 - 600001) * 156.25;
        } else if (height < 900001) {
            coins = height * 39 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5 + (700001 - 600001) * 156.25 + (800001 - 700001) * 78;
        } else if (height < 1000001) {
            coins = height * 19.5 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5 + (700001 - 600001) * 156.25 + (800001 - 700001) * 78 + (900001 - 800001) * 39;
        } else if (height < 3102401) {
            coins = height * 8 + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5 + (700001 - 600001) * 156.25 + (800001 - 700001) * 78 + (900001 - 800001) * 39 + (1000001 - 900001) * 19.5;
        } else {
            var halvings = Math.floor(height - 3102400 / 2102400);
            if (halvings >= 64) {
                return 0;
            }
            var subsidy = 4
            subsidy = subsidy / Math.pow(2, halvings)
            coins = height * subsidy + 200001 * 12500 + (300001 - 200001) * 2500 + (400001 - 300001) * 1250 + (500001 - 400001) * 625 + (600001 - 500001) * 312.5 + (700001 - 600001) * 156.25 + (800001 - 700001) * 78 + (900001 - 800001) * 39 + (1000001 - 900001) * 19.5 + (3102401 - 1000001) * 8;
        }

        return (new BigNumber(0)).plus(coins.toString());
    }

};
